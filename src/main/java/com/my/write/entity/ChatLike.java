package com.my.write.entity;

public class ChatLike {
	public int chat_like = 0;
	public String user_nick = null;
	public String created_date = null;

	public int getChat_like() {
		return chat_like;
	}

	public void setChat_like(int chat_like) {
		this.chat_like = chat_like;
	}

	public String getUser_nick() {
		return user_nick;
	}

	public void setUser_nick(String user_nick) {
		this.user_nick = user_nick;
	}

	public String getCreated_date() {
		return created_date;
	}

	public void setCreated_date(String created_date) {
		this.created_date = created_date;
	}

}
